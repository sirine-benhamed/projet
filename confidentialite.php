                    <h2 class="article_title">5. Combien de temps sont conservées vos Données personnelles ?</h2></br>
                    <hr>
                    <p class="p">
                        DonsSolidaires conserve vos Données personnelles pendant la durée de la relation contractuelle.
                        Dès lors que vous résiliez les services, l’ensemble de vos Données personnelles sont
                        définitivement supprimées, à l’exception de celles devant être conservées par DonsSolidaires au
                        titre d’obligations légales ou pour des motifs légitimes. Ainsi, les durées de conservation
                        spécifiques suivantes vont s’appliquer :
                    <ul>
                         <li>Au titre de ses obligations en matière de lutte contre le blanchiment de capitaux et le financement du terrorisme, DonsSolidaires conserve pendant cinq (5) ans à compter de la résiliation des services les documents et informations relatifs à votre identité.</li>
                         <li>Les données nécessaires à la gestion d’éventuelles contestations ou litiges sont également conservées pendant une durée de cinq (5) ans, conformément aux dispositions légales en vigueur (notamment mais non exclusivement celles prévues par le code de commerce, le code civil et le code de la consommation).</li>
                         <li>Les documents contractuels sont conservés pendant une durée de cinq (5) ans à compter de la fin du contrat.</li>
                         <li>Les communications avec le support client peuvent également être enregistrées. Le cas échéant, elles seront conservées pendant une durée de cinq (5) ans à compter de leur réception ou de leur enregistrement.</li>
                         <li>Les informations permettant de prendre en compte votre droit d'opposition à de la prospection commerciale sont conservées trois (3) ans à compter de l'exercice du droit d'opposition.</li>
                    </ul>
                    </p>
